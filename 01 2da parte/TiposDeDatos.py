# str es una cadena de caracteres
texto: str = "Hola mundo"

# int es un entero
entero: int = 10

# float es un numero decimal
flotante: float = 10.5

# bool es un valor booleano
booleano: bool = True

# list es una lista: tienen un indice
lista: list = [1, 2, 3, 4, 5]

# tuple es una tupla: no tienen indice, no se puede modificar
tupla: tuple = (1, 2, 3, 4, 5)

# set es un conjunto: no tiene indice,son pares de valor
diccionario: dict = {"nombre": "Juan", "apellido": "Perez"}

# set es un conjunto ordenado de elementos unicos, no tiene elementos repetidos
seteo: set = {1, 2, 3, 4, 5}

